#include <iostream>
using namespace std;

int main() {
    int n = 0;          // переменная для номера элемента прогрессии
    double sum = 0.0;   // переменная для суммы элементов прогрессии

    // цикл, пока сумма не превысит 1500
    while (sum <= 1500) {
        n++; // увеличение номера элемента прогрессии

        // вычисление текущего элемента прогрессии
        double an = (3.0 * n * n - 2.0 * n + 12.0) / (n + 1);

        // добавление текущего элемента к сумме
        sum += an;

        // вывод промежуточных данных (опционально)
        cout << "n = " << n << ", a_n = " << an << ", sum = " << sum << endl;
    }

    // вывод результата
    cout << "Номер элемента, при котором сумма превысит 1500: " << n << endl;

    return 0;
}
